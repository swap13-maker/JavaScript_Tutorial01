// while loop
// it is used to iterate part of program repeatedly until the specified condition is true
// if the number of iteration is not fixed we used while loop

//using array
let myArray = ["superman", "batman", "flash", "wonder women"]
let arr=0; 
while (arr < myArray.length) {
    console.log(`value is ${myArray[arr]}`);
    arr++;
}

//using number
let index = 0;
while (index <= 10) {
    console.log(`value is ${index}`);
    index++;
}